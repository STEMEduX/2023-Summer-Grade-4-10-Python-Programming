li = [1, 11, 3, 4, 5]
print(li)
li.append('a') # [1, 11, 3, 4, 5, 'a']
print(li)
li.insert(2, 'i') # [1, 11, 'i', 3, 4, 5, 'a']
print(li)
li = ['a', 'b', 'c', 'd']
print(li)
li.index('b') # 1 - index of first occurence
print(li)
li.count('b') # 2 - number of occurences
print(li)
li.remove('b') # ['a', 'c', 'd'] - remove first occurence
print(li)
li = [5, 2, 6, 8]
print(li)
li.reverse() # reverse the list in place
print(li)
li.sort() # sort the list in place
print(li)
